        #trafo_model = trafo_models[trafo_model_index]
        #trafo_loading = trafo_loadings[trafo_loading_index]

        gpkg_path = self.parameterAsFileOutput(
                                                parameters,
                                                self.OUTPUT,
                                                context
                                            )

        # Run your solver
        feedback.pushInfo("Running powerflow solver…")

        result_layers = run_dc_powerflow(
            layers=layers,
            load_layers=load_layers
        )

        processing.run("native:package", 
                    {'LAYERS':result_layers,
                     'OUTPUT':gpkg_path,
                     'OVERWRITE':False,
                     'SAVE_STYLES':True,
                     'SAVE_METADATA':True,
                     'SELECTED_FEATURES_ONLY':False,
                     'EXPORT_RELATED_LAYERS':False}
                   )

        return {}
        
