class runDCPowerFlowAlgorithm(QgsProcessingAlgorithm):

    NET_LAYERS = 'NET_LAYERS'
    LOAD_LAYERS = 'LOAD_LAYERS'
    OUTPUT = 'OUTPUT'
    
    def initAlgorithm(self, config):

        self.addParameter(
            QgsProcessingParameterMultipleLayers(
                name=self.NET_LAYERS,
                description='Network Layers',
                layerType=QgsProcessing.TypeVector
            )
        )

        #

        self.addParameter(QgsProcessingParameterBoolean(
            self.LOAD_LAYERS, 'Load Layers?',
            defaultValue=True
        ))

        # Output: list of layer IDs
        self.addParameter(QgsProcessingParameterFileDestination(
                self.OUTPUT,
                'Output GeoPackage',
                fileFilter='GeoPackage (*.gpkg)'
            )
        )

    # -------------------------------------------------------
    # PROCESS
    # -------------------------------------------------------

    def processAlgorithm(self, parameters, context, feedback):

        # MULTIPLE LAYERS
        layers = self.parameterAsLayerList(parameters, self.NET_LAYERS, context)

        if not layers:
            raise QgsProcessingException("No valid input layers selected.")

        # Extract all parameters
        # Read parameters from user input
        net_layers = self.parameterAsLayerList(parameters, self.NET_LAYERS, context)
        load_layers = self.parameterAsBool(parameters, self.LOAD_LAYERS, context)

        #algorithms = ["nr", "iwamoto_mr","bfsw","gs","fdbx","fdxb"]      
